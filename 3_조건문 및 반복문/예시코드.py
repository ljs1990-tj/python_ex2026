# [파이썬 기초: 조건문과 반복문 실습 예제]

# ---------------------------------------------------------
# 1. 조건문 (if) - 상황에 따른 판단
# ---------------------------------------------------------
print("--- [1. 조건문 실습] ---")
weather = input("오늘 날씨는 어떤가요? (비/눈/맑음) ")

if weather == "비":
    print("우산을 챙기세요!")
elif weather == "눈":
    print("눈사람을 만들어봐요!")
elif weather == "맑음":
    print("산책하기 좋은 날씨네요.")
else:
    print("알 수 없는 날씨입니다. 예보를 확인하세요.")
print()


# ---------------------------------------------------------
# 2. 반복문 (for) - 정해진 바구니 안에서 반복
# ---------------------------------------------------------
print("--- [2. for문 실습] ---")

# 리스트를 이용한 반복
students = ["짱구", "철수", "유리"]
for student in students:
    print(student, "학생, 출석 확인되었습니다.")

print("\n--- [3. range() 활용 반복] ---")
# range(5)는 0, 1, 2, 3, 4를 만들어냅니다. (5는 포함 안 됨!)
for i in range(1, 6): # 1부터 5까지
    print(i, "번 학생 성적표 출력 중...")
print()


# ---------------------------------------------------------
# 3. 반복문 (while) - 조건이 참인 동안 무한 반복
# ---------------------------------------------------------
print("--- [4. while문 실습] ---")

coffee = 3 # 커피 3잔 남음
while coffee > 0:
    print("커피가 남았습니다. 판매를 시작합니다.")
    coffee = coffee - 1 # 커피를 한 잔 팔았으므로 개수 줄임
    print("남은 커피는", coffee, "잔입니다.")
    
    if coffee == 0:
        print("커피가 다 떨어졌습니다. 판매를 종료합니다.")
print()


# ---------------------------------------------------------
# 4. 반복문 탈출 (break) - 중간에 멈추기
# ---------------------------------------------------------
print("--- [5. break 실습] ---")

# 비밀번호 맞추기 예제 (최대 3번 시도)
correct_password = "python123"

for attempt in range(1, 4):
    user_input = input("비밀번호를 입력하세요: ")
    
    if user_input == correct_password:
        print("로그인 성공!")
        break # 비밀번호를 맞췄으니 반복문(시도)을 즉시 멈춤
    else:
        print("비밀번호가 틀렸습니다. (시도 횟수:", attempt, "/ 3)")
        
