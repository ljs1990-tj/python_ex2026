# [파이썬 기초: 데이터 꾸러미(리스트, 딕셔너리 등) 실습 예제]

# ---------------------------------------------------------
# 1. 리스트(List) - 수정 가능한 데이터 기차
# ---------------------------------------------------------
print("--- [1. 리스트 실습] ---")
subway = ["푸", "피글렛", "티거"] # 세 칸짜리 리스트 생성

# 데이터 추가 (기차 맨 뒤에 한 칸 더 붙이기)
subway.append("이요르")

# 데이터 수정 (1번 인덱스의 '피글렛'을 '로'로 바꾸기)
subway[1] = "로"

# 데이터 삭제 (원하는 번호의 칸을 삭제)
del subway[2] # 2번 인덱스였던 '티거' 삭제

print("현재 지하철 명단:", subway)
print("전체 인원 수(길이):", len(subway)) # 리스트의 길이를 알려주는 len()
print()


# ---------------------------------------------------------
# 2. 딕셔너리(Dictionary) - 이름표가 붙은 상자
# ---------------------------------------------------------
print("--- [2. 딕셔너리 실습] ---")
# 키(Key) : 값(Value) 형태로 저장
cabinet = {"A-3": "유재석", "B-100": "김종국"}

# 데이터 가져오기 (이름표로 찾기)
print("A-3 사물함 주인:", cabinet["A-3"])

# 데이터 추가 및 수정 (새로운 이름표를 붙이거나 기존 내용을 바꿈)
cabinet["C-20"] = "조세호" # 새로운 데이터 추가
cabinet["A-3"] = "지석진"  # 기존 데이터 수정 (유재석 -> 지석진)

# 데이터 삭제
del cabinet["B-100"]

print("현재 사물함 현황:", cabinet)
print()


# ---------------------------------------------------------
# 3. 튜플(Tuple)과 세트(Set) - 특징만 살펴보기
# ---------------------------------------------------------
print("--- [3. 튜플과 세트 특징] ---")

# 튜플: 소괄호를 쓰며, 한 번 만들면 수정이 안 됨 (안전한 저장용)
menu = ("돈까스", "치즈까스")
print("오늘의 메뉴:", menu)
# menu.append("생선까스") # <-- 주석을 풀고 실행하면 에러가 납니다! (추가/수정 불가)

# 세트: 중복을 허용하지 않음 (중복 제거용)
my_numbers = {1, 2, 2, 3, 3, 3}
print("중복 제거된 숫자들:", my_numbers) # 결과는 {1, 2, 3}만 나옵니다.

