# [파이썬 기초: 다양한 연산자 실습 예제]

# ---------------------------------------------------------
# 1. 산술 연산자 (숫자 계산)
# ---------------------------------------------------------
print("--- [1. 산술 연산자] ---")
a = 10
b = 3

print("더하기 (10 + 3):", a + b)
print("빼기 (10 - 3):", a - b)
print("곱하기 (10 * 3):", a * b)
print("나누기 (10 / 3):", a / b)   # 결과: 3.333... (실수로 나옵니다)
print("몫 구하기 (10 // 3):", a // b) # 결과: 3 (소수점 버림)
print("나머지 구하기 (10 % 3):", a % b) # 결과: 1 (짝수/홀수 판별에 자주 씀)
print("거듭제곱 (10의 3승):", a ** b) # 결과: 1000
print()


# ---------------------------------------------------------
# 2. 비교 연산자 (참/거짓 판단)
# ---------------------------------------------------------
print("--- [2. 비교 연산자] ---")
x = 5
y = 10

print("x와 y가 같은가?:", x == y)      # False
print("x와 y가 다른가?:", x != y)      # True
print("x가 y보다 큰가?:", x > y)       # False
print("x가 y보다 작거나 같은가?:", x <= y) # True
print()


# ---------------------------------------------------------
# 3. 논리 연산자 (조건 묶기)
# ---------------------------------------------------------
print("--- [3. 논리 연산자] ---")
# 자바의 &&, ||, ! 대신 and, or, not을 씁니다.

is_sunny = True
has_umbrella = False

# 둘 다 참이어야 참 (and)
print("외출 가능한가? (맑고 우산 있음):", is_sunny and has_umbrella)

# 하나라도 참이면 참 (or)
print("나갈 수 있나? (맑거나 우산 있음):", is_sunny or has_umbrella)

# 결과를 반대로 뒤집음 (not)
print("우산이 없는 게 맞나?:", not has_umbrella)
print()


# ---------------------------------------------------------
# 4. 멤버십 연산자 (포함 여부 확인)
# ---------------------------------------------------------
print("--- [4. 멤버십 연산자] ---")
# 파이썬에서 매우 자주 쓰이는 편리한 도구입니다.

my_list = [1, 2, 3, 4, 5]
my_string = "Hello Python"

print("리스트에 3이 들어있나?:", 3 in my_list)           # True
print("리스트에 10이 없나?:", 10 not in my_list)       # True
print("문자열에 'Py'가 들어있나?:", "Py" in my_string) # True
print()


