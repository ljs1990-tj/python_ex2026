# [파이썬 기초: 변수와 입출력 실습 예제]

# ---------------------------------------------------------
# 1. 변수에 데이터 담기 (다양한 상자 만들기)
# ---------------------------------------------------------
name = "파이썬"          # 문자열(String): 따옴표로 감쌉니다.
age = 35                # 정수(Integer): 소수점이 없는 숫자입니다.
height = 175.5          # 실수(Float): 소수점이 있는 숫자입니다.
is_fun = True           # 불리언(Boolean): 참/거짓을 나타냅니다. (대문자 필수!)

# ---------------------------------------------------------
# 2. 화면에 출력하기 (print 함수)
# ---------------------------------------------------------
print("--- [1. 변수 출력하기] ---")
print("이름:", name)
print("나이:", age)
print("키:", height)
print("파이썬은 재미있나요?", is_fun)
print() # 빈 줄을 하나 출력합니다.

# ---------------------------------------------------------
# 3. 사용자로부터 입력받기 (input 함수)
# ---------------------------------------------------------
print("--- [2. 사용자 입력받기] ---")

# input() 괄호 안의 문구는 사용자에게 보여주는 안내 메시지입니다.
user_hobby = input("당신의 취미는 무엇인가요? ")
print("당신의 취미는", user_hobby, "이군요!")

# ---------------------------------------------------------
# 4. [주의] 숫자를 입력받아 계산할 때 (형 변환)
# ---------------------------------------------------------
# input()으로 받은 값은 컴퓨터가 무조건 '문자'로 인식합니다.
# 숫자로 계산하고 싶다면 int()를 사용해서 숫자로 변신시켜야 합니다.

print("\n--- [3. 숫자 입력 및 계산하기] ---")
num1 = input("첫 번째 숫자를 입력하세요: ") # 예: 10 입력
num2 = input("두 번째 숫자를 입력하세요: ") # 예: 20 입력

# 문자끼리 더하면? "10" + "20" = "1020"이 됩니다.
print("그냥 더했을 때 (문자 연결):", num1 + num2)

# 숫자로 변환해서 더하면? 10 + 20 = 30이 됩니다.
result = int(num1) + int(num2)
print("숫자로 변환해서 더했을 때:", result)

